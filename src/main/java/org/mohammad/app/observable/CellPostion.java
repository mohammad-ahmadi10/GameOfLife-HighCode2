package org.mohammad.app.observable;

public class CellPostion {
    private int posX;
    private int posY;

    public CellPostion(int posX, int posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public int getPosX() {
        return posX;
    }

    public int getPosY() {
        return posY;
    }
}
